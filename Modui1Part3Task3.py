meters = float(input("10"))

centimeters = meters * 100
decimeters = meters * 10
milimeters = meters * 1000
miles = meters * 0.000621371

print( f"{meters} = {centimeters} ")
print( f"{meters}  = {decimeters} ")
print( f"{meters} = {milimeters} ")
print( f"{meters} = {miles} ")
print( f"{meters} = {miles} ")




